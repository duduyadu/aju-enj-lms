export function LanguageProvider({ children }: { children: React.ReactNode }) {
  const [language, setLanguage] = useState<Language>('vi'); // ê¸°ë³¸ ì–¸ì–´: ë² íŠ¸ë‚¨ì–´

  // localStorageì—ì„œ ì–¸ì–´ ì„¤ì • ë¶ˆëŸ¬ì˜¤ê¸°
  useEffect(() => {
    if (typeof window !== 'undefined') {
      const savedLanguage = localStorage.getItem('aju_lang_v2') as Language;
      if (savedLanguage && (savedLanguage === 'ko' || savedLanguage === 'vi')) {
        setLanguage(savedLanguage);
      }
    }
  }, []);

  // ì–¸ì–´ ì „í™˜
  const toggleLanguage = () => {
    const newLanguage = language === 'ko' ? 'vi' : 'ko';
    setLanguage(newLanguage);
    if (typeof window !== 'undefined') {
      localStorage.setItem('aju_lang_v2', newLanguage);
    }
  };

  // ë²ˆì—­ í•¨ìˆ˜
  const t = (key: string): string => {
    return translations[language][key] || key;
  };

  return (
    <LanguageContext.Provider value={{ language, toggleLanguage, t }}>
      {children}
    </LanguageContext.Provider>
  );
}
